public class DortIslem {
    private class DortIslem {
        private int Topla(int sayi1, int sayi2){
            return sayi1 + sayi2;
        }
        private int Cikar(int sayi1, int sayi2){
            return sayi1 - sayi2;
        }
        private int Carp(int sayi1, int sayi2){
            return sayi1 * sayi2;
        }
        private int Bol(int sayi1, int sayi2){
            return sayi1 / sayi2;
        }
    }
}
